
public interface NestedList {

	/**
	 * 
	 * @return - sum of all nested list values
	 */
	public long getSumOfAllValues() ;
	
	/**
	 * 
	 * @return - Largest value in nested list values
	 */
	public long getLargestValue();
	
	/**
	 * 
	 * @return -  find a value from nested list
	 */
	public boolean findValue(long value);
}
